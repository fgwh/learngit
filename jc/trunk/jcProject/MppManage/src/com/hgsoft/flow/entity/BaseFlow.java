package com.hgsoft.flow.entity;

public class BaseFlow implements java.io.Serializable {
	/**审核状态*/
	private int flowStatus;
	/**是否最新*/
	private boolean latestMark;
	/**pid，同组业务数据的流程唯一标示*/
	private String pid;
	public int getFlowStatus() {
		return flowStatus;
	}
	public void setFlowStatus(int flowStatus) {
		this.flowStatus = flowStatus;
	}
	public boolean isLatestMark() {
		return latestMark;
	}
	public void setLatestMark(boolean latestMark) {
		this.latestMark = latestMark;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
}
