package com.hgsoft.main.entity;

import com.hgsoft.security.entity.BaseEntity;

// Dictionary Item
public class DicItem implements BaseEntity {

	/** 记录ID */
	private String id;

	/** 分类 */
	private String type;

	/** 名字 */
	private String name;

	/** 值 */
	private String value;

	/** 内部分类 */
	private String innerType;

	/** 内部顺序号 */
	private Integer innerOrder;

	/** 备注 */
	private String note;

	public DicItem() {
	}

	public DicItem(String id, String type, String name, String value,
			String innerType, Integer innerOrder, String note) {
		this.id = id;
		this.type = type;
		this.name = name;
		this.value = value;
		this.innerType = innerType;
		this.innerOrder = innerOrder;
		this.note = note;
	}

	/** 记录ID */
	public String getId() {
		return id;
	}

	/** 记录ID */
	public void setId(String id) {
		this.id = id;
	}

	/** 分类 */
	public String getType() {
		return type;
	}

	/** 分类 */
	public void setType(String type) {
		this.type = type;
	}

	/** 名字 */
	public String getName() {
		return name;
	}

	/** 名字 */
	public void setName(String name) {
		this.name = name;
	}

	/** 值 */
	public String getValue() {
		return value;
	}

	/** 值 */
	public void setValue(String value) {
		this.value = value;
	}

	/** 内部分类 */
	public String getInnerType() {
		return innerType;
	}

	/** 内部分类 */
	public void setInnerType(String innerType) {
		this.innerType = innerType;
	}

	/** 内部顺序号 */
	public Integer getInnerOrder() {
		return innerOrder;
	}

	/** 内部顺序号 */
	public void setInnerOrder(Integer innerOrder) {
		this.innerOrder = innerOrder;
	}

	/** 备注 */
	public String getNote() {
		return note;
	}

	/** 备注 */
	public void setNote(String note) {
		this.note = note;
	}

}
