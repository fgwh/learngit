package com.hgsoft.main.recanalysis.entity;

public class Conditions {

	private String centralStation;// 中心站
	private String stationNo; // 收费站
	private String gateWay; // 出入口
	private String period;// 时间段
	private String startQueryDate; //开始自然日
	private String endQueryDate;//  结束自然日
	private String enLane;//车道
	private String churukou;
	private String shijianduan;
	private String orgName;
	
	
	public Conditions() {
		super();
	}
	public String getCentralStation() {
		return centralStation;
	}
	public void setCentralStation(String centralStation) {
		this.centralStation = centralStation;
	}
	public String getStationNo() {
		return stationNo;
	}
	public void setStationNo(String stationNo) {
		this.stationNo = stationNo;
	}
	public String getGateWay() {
		return gateWay;
	}
	public void setGateWay(String gateWay) {
		this.gateWay = gateWay;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public String getStartQueryDate() {
		return startQueryDate;
	}
	public void setStartQueryDate(String startQueryDate) {
		this.startQueryDate = startQueryDate;
	}
	public String getEndQueryDate() {
		return endQueryDate;
	}
	public void setEndQueryDate(String endQueryDate) {
		this.endQueryDate = endQueryDate;
	}
	public String getEnLane() {
		return enLane;
	}
	public void setEnLane(String enLane) {
		this.enLane = enLane;
	}
	public String getChurukou() {
		return churukou;
	}
	public void setChurukou(String churukou) {
		this.churukou = churukou;
	}
	public String getShijianduan() {
		return shijianduan;
	}
	public void setShijianduan(String shijianduan) {
		this.shijianduan = shijianduan;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
    
}
