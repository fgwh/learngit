package com.hgsoft.util;

/**
 * 字符串处理工具
 * 
 * @version 1.0
 * @date 2014-11-24
 * @author WuBiao
 *
 */
public class StringUtil {

	/**
	 * 判断去空格后是否为空
	 * 
	 * @param str
	 * @return
	 */
	public static boolean isTrimEmpty(String str) {
		
		if(null != str && !"".equals(str.trim())) {
			return false;
		}else {
			return true;
		}
	}
}
